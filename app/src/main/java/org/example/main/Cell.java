package org.example.main;

/**
 * Defines coordinates of cell as displayed on the board
 */
public class Cell {
    int x;
    int y;

    /**
     * @param x Cell's X coordinate
     * @param y Cell's Y coordinate
     */
    public Cell(int x, int y) {
        this.x = x;
        this.y = y;
    }
}